mensagem = "Eu adoro comida Caseira"

print(mensagem)

print(mensagem.lower()) # tudo minusculo
print(mensagem.upper()) # tudo maiusculo
print(mensagem.capitalize()) # primeira letra da frase maiuscula
print(mensagem.find('c')) # Procura a letra c na frase e rotorna em qual posicao esta a letra
print(mensagem.replace('Eu', "Ele")) # Subistitui o primeiro campo pelo segundo campo
print(mensagem.strip()) # remove os espacos em branco antes do primeiro caracter

